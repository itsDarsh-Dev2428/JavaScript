
let age = 5;
let grace = 2;


// arthematic operators
console.log(age + grace); // addition
console.log(age - grace); // subtraction
console.log(age * grace); // multiplication
console.log(age / grace); // division
console.log(age % grace); // modulus
console.log(age ** grace); // exponentiation
age+=grace; // increment age by grace
age-=grace; // decrement age by grace
console.log(age);

// comparison operators
console.log(age == grace); // loose equality
console.log(age === grace); // strict equality
console.log(age != grace); // loose inequality
console.log(age !== grace); // strict inequality
console.log(age > grace); // greater than 
console.log(age < grace); // less than
console.log(age >= grace); // greater than or equal to
console.log(age <= grace); // less than or equal to



// logical operators
console.log(age > 18 && grace < 5); // AND operator
console.log(age > 18 || grace < 5); // OR operator
console.log(!(age > 18)); // NOT operator
console.log(age > 18 ? "You can drive" : "You cannot drive"); // ternary operator

